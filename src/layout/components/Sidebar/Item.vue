<script>
import { h } from 'vue'

export default {
  props: {
    icon: {
      type: String,
      default: ''
    },
    title: {
      type: String,
      default: ''
    }
  },
  render() {
    const vnodes = []
    if (this.icon) {
      if (this.icon.includes('el-icon')) {
        vnodes.push(h('i', { class: [this.icon, 'sub-el-icon'] }))
      } else {
        vnodes.push(h('svg-icon', { 'icon-class': this.icon }))
      }
    }

    if (this.title) {
      vnodes.push(h('span', { slot: 'title' }, this.title))
    }

    return h('div', null, vnodes)
  }
}
</script>

<style scoped>
.sub-el-icon {
  color: currentColor;
  width: 1em;
  height: 1em;
}
</style>
